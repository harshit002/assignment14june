package stringHandling;



public class stringpattern {

	public static void main(String[] args) 
	
     {
		char ui=(char)97;
		for(int i=0;i<3;i++)
			{
			  for(int j=0;j<=i;j++)
			  {
				  ui=(char)(97+j);
				 System.out.print(ui); 
			  }
			   
			  System.out.println();
		     }
		
	
		}

	}


